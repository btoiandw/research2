            ทั้งนี้หากมีการนำผลงาน วิจัยไปใช้ประโยชน์เพื่อการค้าหรือเชิงพาณิชย์ ให้เป็นไปตามข้อตกลงที่ ผู้ให้ทุนกำหนด
