            และจะประพฤติและปฏิบัติตามระเบียบ ข้อบังคับ หรือคำสั่งของมหาวิทยาลัยราชภัฏกำแพงเพชรดังกล่าวอย่างเคร่งครัด
            ทั้งที่ได้ออกใช้บังคับอยู่แล้วก่อนวันทำสัญญาและจะออกใช้บังคับ ต่อไปในภายภาคหน้า
            โดยผู้รับทุนและผู้ให้ทุนตกลงยินยอม ถือว่าระเบียบข้อบังคับหรือคำสั่งต่างๆ ดังกล่าว
