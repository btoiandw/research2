            หากผู้รับทุนไม่ปฏิบัติตามระยะเวลาที่กำหนด ดังกล่าว
            ผู้ให้ทุนมีสิทธิบอกเลิกสัญญาได้ทันที และผู้รับทุนจะต้องชดใช้คืนเงินทุนทั้งหมดที่ได้รับไป
            รวมทั้งดอกผลที่เกิดจากเงินทุนนั้น ตลอดจนอุปกรณ์การวิจัยทั้งหมดแก่ผู้ให้ทุนภายใน 30 วัน
            นับแต่วันที่ผู้รับทุนได้รับหนังสือแจ้งบอกเลิกสัญญาจากผู้ให้ทุน
